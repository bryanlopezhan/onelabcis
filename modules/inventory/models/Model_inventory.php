class Model_inventory extends MY_Model {

    private $primary_key    = 'chemical_code';
    private $table_name     = 'inventory';
    public $field_search   = ['chemical_code', 'name_of_pure_chemical', 'chemical_formula', 'location', 'date_registered', 'registered_by', 'state_of_chemical', 'chemical_color_group', 'cas_no', 'brand', 'lot_no', 'container', 'storage_condition', 'original_amount', 'estimated_current_mount', 'date_of_receipt', 'received_by', 'date_of_expiration'];
    public $sort_option = ['chemical_code', 'DESC'];
    
    public function __construct()
    {
        $config = array(
            'primary_key'   => $this->primary_key,
            'table_name'    => $this->table_name,
            'field_search'  => $this->field_search,
            'sort_option'   => $this->sort_option,
         );

        parent::__construct($config);
    }

    public function count_all($q = null, $field = null)
    {
        $iterasi = 1;
        $num = count($this->field_search);
        $where = NULL;
        $q = $this->scurity($q);
        $field = $this->scurity($field);

        if (empty($field)) {
            foreach ($this->field_search as $field) {
                $f_search = "inventory.".$field;

                if (strpos($field, '.')) {
                    $f_search = $field;
                }
                if ($iterasi == 1) {
                    $where .=  $f_search . " LIKE '%" . $q . "%' ";
                } else {
                    $where .= "OR " .  $f_search . " LIKE '%" . $q . "%' ";
                }
                $iterasi++;
            }

            $where = '('.$where.')';
        } else {
            $where .= "(" . "inventory.".$field . " LIKE '%" . $q . "%' )";
        }

        $this->join_avaiable()->filter_avaiable();
        $this->db->where($where);
        $query = $this->db->get($this->table_name);

        return $query->num_rows();
    }

